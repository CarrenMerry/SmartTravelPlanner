document.addEventListener('DOMContentLoaded', () => {
    initBlogDetailPage();
});

function initBlogDetailPage() {
    const params = new URLSearchParams(window.location.search);
    const blogId = params.get('id');
    const blog = blogs[blogId];

    if (!blog) {
        showBlogErrorState();
        return;
    }

    populateBlogDetail(blog);
    setupBlogAnimations();
    setupNavbarScroll();
}

function populateBlogDetail(blog) {
    document.title = `${blog.title} | Smart Travel`;

    const heroBg = document.getElementById('blog-hero-bg');
    heroBg.style.backgroundImage = `url('${blog.image}')`;
    setTimeout(() => heroBg.classList.add('active'), 120);

    document.getElementById('blog-title').innerText = blog.title;
    document.getElementById('blog-subtitle').innerText = blog.subtitle;
    document.getElementById('blog-destination-chip').innerText = blog.destination;
    document.getElementById('blog-read-time').innerText = blog.readTime;
    document.getElementById('blog-category').innerText = blog.category;
    document.getElementById('blog-destination').innerText = blog.destination;
    document.getElementById('blog-intro').innerText = blog.intro;
    document.getElementById('sidebar-destination').innerText = blog.destination;
    document.getElementById('sidebar-mood').innerText = blog.mood;
    document.getElementById('sidebar-best-for').innerText = blog.bestFor;
    document.getElementById('plan-trip-btn').href = `plan.html?destination=${encodeURIComponent(blog.destination)}`;

    const contentContainer = document.getElementById('blog-content');
    contentContainer.innerHTML = blog.content.map(block => {
        if (block.image) {
            return `
                <figure class="inline-media">
                    <img src="${block.image}" alt="${blog.title}">
                    <figcaption class="inline-caption">${block.caption}</figcaption>
                </figure>
            `;
        }

        return `
            <section>
                <h2>${block.heading}</h2>
                ${block.paragraphs.map(paragraph => `<p>${paragraph}</p>`).join('')}
            </section>
        `;
    }).join('');

    const tipsContainer = document.getElementById('blog-tips');
    tipsContainer.innerHTML = blog.tips.map(tip => `<li>${tip}</li>`).join('');
}

function setupBlogAnimations() {
    const fadeElements = document.querySelectorAll('.section-fade-load');

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, {
        threshold: 0.12,
        rootMargin: '0px'
    });

    fadeElements.forEach(element => observer.observe(element));

    requestAnimationFrame(() => {
        fadeElements.forEach((element, index) => {
            setTimeout(() => element.classList.add('visible'), index * 90);
        });
    });
}

function setupNavbarScroll() {
    const nav = document.querySelector('.navbar');

    if (!nav) return;

    window.addEventListener('scroll', () => {
        if (window.scrollY > 40) {
            nav.classList.add('scrolled');
        } else {
            nav.classList.remove('scrolled');
        }
    });
}

function showBlogErrorState() {
    document.body.innerHTML = `
        <div class="container text-center py-5 mt-5">
            <h1 class="display-1 fw-bold text-white">404</h1>
            <p class="fs-3 text-white-50">This blog article could not be found.</p>
            <a href="index.html#blog" class="btn-explore mt-4">Back to Blogs</a>
        </div>
    `;
}
